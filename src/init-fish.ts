export function renderFishSnippet(): string {
  // 「@ 」モード中だけ補完関数を毎キー再実行するため、英数・パス区切り文字を bind する。
  // fish は通常 complete を1回しか呼ばないので、これがないと fuzzy が初回しか効かない。
  const RECOMPLETE_CHARS = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789._-/";
  const bindings = [...RECOMPLETE_CHARS]
    .map((c) => `bind ${quote(c)} '__cmux_open_file_self_insert ${quote(c)}'`)
    .join("\n");

  return `# Auto-generated by cmux-open-file. Do not edit directly.

# '@' を空のプロンプトで押すと「@ 」を入れて即座に補完を発火させる。
# 既に入力中なら通常の '@' 入力。
bind @ '__cmux_open_file_at_trigger'

function __cmux_open_file_at_trigger
    if test -z (commandline)
        commandline -i '@ '
        # complete-and-search を使うと候補が1つに絞れても自動確定されない (pager が必ず開く)
        commandline -f complete-and-search
    else
        commandline -i '@'
    end
end

# '@' コマンド本体: 引数 (ファイルパス) を受け取って cmux に展開
function @
    cmux-open-file open $argv
end

# '@' コマンドの dynamic completion (fuzzy ランクを keep-order で維持)
complete -c @ -f -k -a '(cmux-open-file complete -- (commandline -ct))'

# 文字キーを「挿入 + (必要なら) 補完再発火」に差し替え。'@ ' で始まる行のときだけ
# complete を再実行する。それ以外のコマンド入力では普通の挿入として振る舞う。
function __cmux_open_file_self_insert
    commandline -i $argv[1]
    if string match -q '@ *' -- (commandline)
        commandline -f complete-and-search
    end
end

# Backspace も同様に、削除後にまだ '@ ' モードなら再補完。
function __cmux_open_file_backspace
    commandline -f backward-delete-char
    if string match -q '@ *' -- (commandline)
        commandline -f complete-and-search
    end
end
bind \\b '__cmux_open_file_backspace'
bind \\x7f '__cmux_open_file_backspace'

${bindings}
`;
}

function quote(c: string): string {
  // single-quote-escape: ' -> '\''
  return `'${c.replace(/'/g, "'\\''")}'`;
}
